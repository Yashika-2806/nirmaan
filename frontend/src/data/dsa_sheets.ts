export const getQuestions = (sheetId: string) => {
    // 1. NeetCode 150
    if (sheetId === 'neetcode-150' || sheetId === 'blind-75') {
        const questions: any[] = [];
        let idCounter = 1;

        Object.entries(NEETCODE_DATA).forEach(([topic, problems]) => {
            Object.entries(problems).forEach(([title, data]) => {
                questions.push({
                    id: String(idCounter++),
                    title,
                    difficulty: data.difficulty,
                    topic,
                    status: 'pending',
                    sheet: sheetId,
                    leetcodeUrl: data.url
                });
            });
        });

        if (sheetId === 'blind-75') {
            // Filter by Blind 75 Titles (fuzzy match or exact match)
            return questions.filter(q => BLIND_75_TITLES.includes(q.title) || BLIND_75_TITLES.some(bt => q.title.includes(bt) && Math.abs(q.title.length - bt.length) < 5));
        }
        return questions;
    }

    // 2. Love Babbar 450 (Sample)
    if (sheetId === 'love-babbar') {
        // Return sample + placeholder message
        return LOVE_BABBAR_SAMPLE.map((q, i) => ({
            id: String(i + 1),
            title: q.title,
            difficulty: q.difficulty,
            topic: q.topic as string, // Cast to string
            status: 'pending',
            sheet: 'love-babbar',
            leetcodeUrl: q.url
        }));
    }

    // 3. Grind 75 (Fallback to Blind 75 logic or empty)
    if (sheetId === 'grind-75') {
        // Grind 75 is similar to Blind 75 but updated. Tag all questions as 'grind-75'
        return getQuestions('blind-75').map(q => ({ ...q, sheet: 'grind-75' }));
    }

    return [];
};
